package site.hardware.wx.bean;

public class Discount {
	private int line;
	private int reduce;
	private int id;
	/**
	 * @return the line
	 */
	public int getLine() {
		return line;
	}
	/**
	 * @param line the line to set
	 */
	public void setLine(int line) {
		this.line = line;
	}
	/**
	 * @return the id
	 */
	public int getId() {
		return id;
	}
	/**
	 * @param id the id to set
	 */
	public void setId(int id) {
		this.id = id;
	}
	/**
	 * @return the reduce
	 */
	public int getReduce() {
		return reduce;
	}
	/**
	 * @param reduce the reduce to set
	 */
	public void setReduce(int reduce) {
		this.reduce = reduce;
	}
}
